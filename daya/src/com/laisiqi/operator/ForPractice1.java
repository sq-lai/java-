package com.laisiqi.operator;
//需求：求1-5之间的数据和，并把求和结果在控制台输出
public class ForPractice1 {
    public static void main(String[] args) {
            int sum = 0;
            for (int i = 1;i <= 5;i++){

                sum = sum + i;






            }

        System.out.println("1-5之间数字的和是：" + sum);





    }







}
